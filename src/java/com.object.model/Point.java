package com.object.model;

import javax.persistence.*;

@Entity
public class Point {
    private int x;
    private int y;

    Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    @Override
    public String toString(){
        return String.format("(%d, %d)",x,y);
    }
}